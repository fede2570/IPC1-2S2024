    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        try {
            File archivo = null;
            FileReader fileR = null;
            BufferedReader bufferedReader = null;
            
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.showOpenDialog(null);
            archivo = fileChooser.getSelectedFile();
            fileR = new FileReader(archivo);
            bufferedReader = new BufferedReader(fileR);
            
            String linea;
            boolean omitirEncabezado = true;
            
            while ((linea = bufferedReader.readLine()) != null) {
                if (omitirEncabezado == true) {
                    omitirEncabezado = false;
                    continue;
                }
                /*
                codigo, descripcion,patron
                M1,Muestra1,1;2;3;4
                */
                String arreglo[] = linea.split(",");
                //Codigo = arreglo[0]
                //Descripcion = arreglo[1]
                //Patron = arreglo[2]
                String arregloPatron[] = arreglo[2].split(";");
                
                //Formula para obtener tama√±o de filas y columnas
                //len(filas o columnas) = raiz(len(arreglo separado por punto y coma))
                int longitud = (int) Math.sqrt(arregloPatron.length);
                //Crear matriz
                int matriz[][] = new int[longitud][longitud];
                int contador = 0;
                
                for (int i = 0; i < longitud; i++) {
                    for (int j = 0; j < longitud; j++) {
                        matriz[i][j] = Integer.parseInt(arregloPatron[contador]);
                        contador++;
                    }
                }
                Muestras muestra = new Muestras(arreglo[0], arreglo[1], "Ingreso", matriz);
                Ejemplo_Proyecto.lista_muestras.add(muestra);
            }
            
        } catch(Exception e) {
            
        } 
    }//GEN-LAST:event_jButton5ActionPerformed
 
