    public Muestras buscarMuestra(String codigo) {
        for (Muestras mu: Ejemplo_Proyecto.lista_muestras) {
            if (mu.codigo.equals(codigo)) {
                return mu;  //Retorna el objeto muestra
            }
        }
        return null;
    }
    
    public void reporteHtml(String codigo) {
        
        try {
            String ruta = codigo + ".html";
            File archivo = new File(ruta);
            
            if (archivo.exists() == false) { //archivo no existe
                archivo.createNewFile();
            }
            FileWriter fileWriter = new FileWriter(archivo);
            BufferedWriter buffer = new BufferedWriter(fileWriter);
            
            String html = "<html>"
                    + "<body>"
                    + "<table border='1'>";
            //For para recorrrer filas
            for (int i = 0; i < buscarMuestra(codigo).matriz.length; i++) {
                html += "<tr>";
                //For para recorrer columnas
                for (int j = 0; j < buscarMuestra(codigo).matriz[i].length; j++) {
                    html += "<td>" + buscarMuestra(codigo).matriz[i][j] + "</td>";
                }
                html += "</tr>";
            }
            
            html += "</tbody>"
                    + "</body>"
                    + "</html>";
            
            buffer.write(html);
            buffer.close();
            fileWriter.close();
            
        } catch (Exception e) {
            
        }
    }
    
