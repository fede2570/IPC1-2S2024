    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Leer archivo de matriz de nxn
        try {
            File archivo = null;
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.showOpenDialog(null);
            archivo = fileChooser.getSelectedFile();
            
            Scanner scan = new Scanner(archivo);
            //Guardar fila de matriz en arreglo
            /*
            1,2,3
            4,5,6
            7,8,9
            */
            String temp[] = scan.nextLine().split(",");
            int tam = temp.length;      //Tama√±o de 3
            scan.close();
            //abrir de nuevo Scanner
            scan = new Scanner(archivo);
            matriz = new int[tam][tam];
            
            for (int i = 0; i < tam; i++) {
                String []valor = scan.nextLine().split(",");
                for (int j = 0; j < tam; j++) {
                    //Asignarle valores a matriz
                    matriz[i][j] = Integer.parseInt(valor[j]);
                }
            }
            scan.close();
            
        } catch(Exception e) {
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

